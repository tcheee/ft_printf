int		check_error(const char *f, int i)
{
	int	j;
	int	b;

	j = i;
	if (f[j] == 'c' || f[j] == 's' || f[j] == 'p'
			|| f[j] == 'd' || f[j] == 'i' || f[j] == 'o'
			|| f[j] == 'u' || f[j] == 'x' || f[j] == 'X'
			|| f[j] == 'f' || f[j] == '%' || f[j] == 'l' || f[j] == 'h')
	{
		b = 1;
		while (f[j] == 'c' || f[j] == 's' || f[j] == 'p'
				|| f[j] == 'd' || f[j] == 'i' || f[j] == 'o'
				|| f[j] == 'u' || f[j] == 'x' || f[j] == 'X'
				|| f[j] == 'f' || f[j] == '%' || f[j] == 'l' || f[j] == 'h')
			j++;
	}
	else
	{
		b = -1;
		while (f[j] != 'c' && f[j] != 's' && f[j] != 'p'
				&& f[j] != 'd' && f[j] != 'i' && f[j] != 'o'
				&& f[j] != 'u' && f[j] != 'x' && f[j] != 'X'
				&& f[j] != 'f' && f[j] != '%' && f[j] != '\0')
		{
			if (f[j] == '.' || f[j] == '+' || f[j] == '-'|| f[j] == '#'||
					f[j] == ' ')
				b = 1;
			if (f[j] >= '0' && f[j] <= '9')
				b = 1;
			j++;
		}
	}
	return (b);
}
