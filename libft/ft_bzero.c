#include "libft.h"

void	ft_bzero(void *b, size_t n)
{
	unsigned char	*ptr;
	size_t			i;

	ptr = (unsigned char *)b;
	i = 0;
	while (i < n)
	{
		ptr[i] = '\0';
		i++;
	}
}
